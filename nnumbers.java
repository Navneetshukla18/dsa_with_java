import java.util.*;
public class nnumbers {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int counter=0;
        int n = sc.nextInt();
        while(counter<=n){
            System.out.print(counter+" ");
            counter++;
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
}
